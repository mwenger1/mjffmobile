			<h3>Should I start taking medications right away?</h3>
			<p>
				You and your doctor should decide together on the right course of treatment for you, including when to begin with medications and what other forms of therapy you might wish to pursue. Decisions must be made about whether and which medications to try first. And to some extent, determining what is right for you is a process of trial and error.
			</p>
			<p>
				Don't feel pressured into making a decision right away. If you have Parkinson’s disease, you had it yesterday and you will have it tomorrow. You have time to educate yourself and decide how you want to treat your disease and yourself.
			</p>
			<p>
				Things to take into consideration: Starting on medication right away can limit your ability to participate in clinical trials that urgently need newly-diagnosed, unmedicated Parkinson’s patients. No drug on today's market has been proven to slow or stop Parkinson’s disease. Available drugs only mask the symptoms. Weigh the importance of symptomatic relief against the side effects of the medications, your doctor's advice, and your own feelings about taking medication.
