					<p>
						As hockey dad and president of the hockey board, Dave has helped the program grow and succeed over the years. Now, the team is saying thank you in a big way. On February 1, they are turning their hockey game into a fundraiser and awareness builder for Parkinson's disease, with which Dave was diagnosed five years ago. According to his youngest son Travis, "It's a lot more than a game. It's something to play for, the game has a purpose. This disease has affected our family a tremendous amount and it's important that people are more aware of the disease and its effects. It will be a very emotional night."
					</p>
					<p>
						Dave considers himself the type of person who is always looking to help others and it's touching to him that the team wants to help. "I am honored," he says. "This is such a humbling experience. Usually when someone has a disease, people don't like to talk about it. With this event, we can increase awareness of Parkinson's so that the community is more comfortable talking about it. And hopefully we can raise funds to make an impact on research."
